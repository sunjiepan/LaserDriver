void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART1)
	{
			RxWrite((uint8_t*)aRxBuffer, RXBUFFERSIZE);
			HAL_UART_Receive_DMA(&huart1, (uint8_t*)aRxBuffer, RXBUFFERSIZE);
	}
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance == USART1)
	{
		if(TxRead(aTxBuffer, RXBUFFERSIZE) > 0)
		HAL_UART_Transmit_DMA(&huart1, aTxBuffer, sizeof(CustomProtocol_TypeDef));
	}
}

void UART_IDLE_Callback(UART_HandleTypeDef *huart)
{
	uint16_t Rxlen = 0;
	
	if((__HAL_UART_GET_FLAG(huart, UART_FLAG_IDLE) != RESET) && (__HAL_UART_GET_IT_SOURCE(huart, UART_IT_IDLE) != RESET))
	{
		__HAL_UART_CLEAR_IDLEFLAG(huart);
			/* set uart state  ready*/
			huart->RxState = HAL_UART_STATE_READY;
			
			/* Disable the rx  DMA peripheral */
		__HAL_DMA_DISABLE(huart->hdmarx);
		
		 /*Clear the DMA Stream pending flags.*/
		__HAL_DMA_CLEAR_FLAG(huart->hdmarx,__HAL_DMA_GET_TC_FLAG_INDEX(huart->hdmarx));
		
		/* get rx data len */
		Rxlen = RXBUFFERSIZE - huart->hdmarx->Instance->CNDTR;
		RxWrite((uint8_t*)aRxBuffer, Rxlen);
		
		/* Process Unlocked */
		__HAL_UNLOCK(huart->hdmarx);
		
		HAL_UART_Receive_DMA(huart,(uint8_t*)aRxBuffer,RXBUFFERSIZE);	

	}
}
